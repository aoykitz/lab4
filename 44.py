# Наивный алгоритм поиска подстроки в строке
def naive_search(text, pattern):
    positions = []
    n = len(text)
    m = len(pattern)
    
    for i in range(n - m + 1):
        match = True
        for j in range(m):
            if text[i + j] != pattern[j]:
                match = False
                break
        if match:
            positions.append(i)
    
    return positions

# Бинарный поиск в отсортированном списке
def binary_search(terms, search_term):
    low = 0
    high = len(terms) - 1
    results = []
    
    while low <= high:
        mid = (low + high) // 2
        current_term = terms[mid][0].lower()
        
        if current_term.startswith(search_term.lower()):
            # Нашли совпадение, ищем все подходящие термины в обе стороны
            results.append(terms[mid])
            
            # Поиск слева
            i = mid - 1
            while i >= 0 and terms[i][0].lower().startswith(search_term.lower()):
                results.append(terms[i])
                i -= 1
            
            # Поиск справа
            i = mid + 1
            while i < len(terms) and terms[i][0].lower().startswith(search_term.lower()):
                results.append(terms[i])
                i += 1
            
            break
        elif terms[mid][0].lower() < search_term.lower():
            low = mid + 1
        else:
            high = mid - 1
    
    return results

