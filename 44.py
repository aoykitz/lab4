# Парсинг словаря и извлечение терминов
def parse_dictionary(text):
    terms = []
    lines = text.split('\n')
    
    for line in lines:
        line = line.strip()
        if line and ',' in line and line[0].isupper() and not line.startswith(' '):
            # Извлекаем термин и определение
            parts = line.split(',', 1)
            term = parts[0].strip()
            definition = parts[1].strip()
            terms.append((term, definition))
    
    return terms

# Поиск терминов в словаре
def search_in_dictionary(terms, search_term, use_binary=True):
    if use_binary:
        return binary_search(terms, search_term)
    else:
        # Используем наивный поиск для поиска по началу терминов
        results = []
        for term, definition in terms:
            if term.lower().startswith(search_term.lower()):
                results.append((term, definition))
        return results

